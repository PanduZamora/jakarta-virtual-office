                        <h1 data-lang="sv">Design</h1>
                        <div data-lang="sv">
                            <p><strong>Formgivning</strong>, eller <strong>design</strong>, är ett begrepp som beskriver ett föremåls konstruktion, utseende eller den skapandeprocess som används från idéstadiet till färdig produkt. Dessa produkter utvecklas efter funktionella och estetiska krav med utgångspunkt från brukarens behov. Design tillämpas för utveckling av varor, tjänster, processer och miljöer. Då inkluderas funktioner som teknik, användarvänlighet, miljövänlighet, utseende, affärslösningar och även marknadsföring. Det finns emellertid ingen allmänt accepterad definition av ordet design, då termen har olika betydelser inom olika designdiscipliner och användarområden.</p>

                            <p>Ordet design kommer från latinets <i>designo</i> = peka ut, avbilda, framställa. Design är en engelsk term med den ursprungliga innebörden för plan, skiss, ritning, vilken motsvarades av <i>desseIn</i> i 1600-talets Sverige. År 1650 sägs exempelvis att drottning Kristina hade fullföljd sin faders "högberömlige och förnäme desseiner". Man syftade då på den pågående stadsplaneringen i Stockholm och att Gustav II Adolf själv aktivt hade påverkat den.</p>

                            <p>Formgivning/design är den ändamålsenliga utformningen genom en formgivare/designer av hantverksmässigt eller industriellt framställda produkter och miljöer Formgivning används för att beskriva gestaltningen av hantverk eller industriellt framställda produkter och miljöer. Syftet med formgivning och design är huvudsakligen att ge ett föremål eller en miljö en väl fungerade form, att underlätta dess användning, att optimera materialåtgången och att ge den ett tilltalande yttre.</p>
                        </div>

                        <h1 data-lang="no">Design</h1>
                        <div data-lang="no">
                            <p><strong>Design</strong> er et internasjonalt begrep som både betegner skaperprosessen av en gjenstand eller et produkt med tanke på formgiving og funksjon, og selve resultatet av denne prosessen. Betegnelsen brukes ofte innen håndverk, industri og kunstindustri, og den kan deles opp i blant annet klesdesign, møbeldesign, industridesign og grafisk design. Design har tradisjonelt blitt plassert i grenselandet mellom håndverk og kunst. Betydningen av design som konkurransevirkemiddel avspeiles i begrepet strategisk design, som er utviklet etter år 2000.</p>
                            <p>En designer er en som har design som profesjon. I Norge kan man utdanne seg innen designfag ved flere læresteder, blant andre Arkitektur- og designhøgskolen AHO og NTNU (industriell design), Kunsthøgskolen i Oslo KHIO og Kunst- og designhøgskolen i Bergen KHiB (møbel- og klesdesign), Oslomet (produktdesign) og flere andre læresteder innen andre designretninger. Det er også mulig å gå yrkesrettet videregående skole i utdanningsprogrammet Design og håndverk, der man går to år på skole fulgt av to år i lære i en bedrift.</p>

                            <p>Norge har en egen lov av 14. mars 2003 nr. 15 om beskyttelse av design, ofte kalt designloven. Der er design definert i § 2 nr. 1 som «utseendet til produkt eller en del av et produkt som særlig følger av de karakteriserende trekkene ved linjene, konturene, fargene, formen, strukturen eller materialet til produktet eller produktets ornamentering.»</p>
                        </div>


                        <h1 data-lang="ru">Дизайн</h1>
                        <div data-lang="ru">
                            <p><strong>Диза́йн</strong> (от англ. design — проектировать, чертить, задумать, а также проект, план, рисунок) — деятельность по проектированию эстетических свойств промышленных изделий («художественное конструирование»), а также результат этой деятельности (например, в таких словосочетаниях, как «дизайн автомобиля»)[1].</p>

                            <p>Считается, что в более широком смысле дизайн призван не только к художественному конструированию, но должен участвовать в решении более широких социально-технических проблем функционирования производства, потребления, существования людей в предметной среде, путём рационального построения её визуальных и функциональных свойств.</p>

                            <p>Теоретической основой дизайна является техническая эстетика.</p>

                            <p>Термин <i>«промышленный дизайн»</i> был утверждён решением первой генеральной ассамблеи ICSID (International Council of Societies of Industrial Design, Международного совета организаций промышленного дизайна) в 1959 году; термин «дизайн» является профессиональным сокращением термина «промышленный дизайн».</p>
                        </div>

                        <h1 data-lang="es">Diseño</h1>
                        <div data-lang="es">
                            <p><strong>El diseño</strong> se define como el proceso previo de configuración mental, "prefiguración", en la búsqueda de una solución en cualquier campo. Se aplica habitualmente en el contexto de la industria, ingeniería, arquitectura, comunicación y otras disciplinas que requieren creatividad. El diseño involucra variadas dimensiones que van más allá del aspecto, la forma y el color, abarcando también la función de un objeto y su interacción con el usuario. Durante el proceso se debe tener en cuenta además la funcionalidad, la operatividad, la eficiencia y la vida útil del objeto del diseño.</p>

                            <p>Durante décadas los vínculos entre el diseño y los movimientos de vanguardia se convirtieron en el centro del debate entre investigadores y expertos y alejaron la mirada de otros aspectos más relevantes. El diseño guarda relación con la actividad artística en la medida que emplea un lenguaje similar, que utiliza una sintaxis prestada de las artes plásticas, pero es un fenómeno de naturaleza más compleja y enteramente vinculado a la actividad productiva y al comercio.</p>

                            <p>Como subrayaba, “a diferencia del arte y la arquitectura donde el protagonista son los artefactos, el proceso histórico del diseño no se basa sólo en los proyectistas, porque al menos un peso similar tienen los productores, los vendedores y el mismo público”.</p>

                            <p>Se suele confundir con frecuencia a los diseñadores y a los artistas, aunque únicamente tienen en común la creatividad. El diseñador proyecta el diseño sobre la base de una inspiración, justificando sus propuestas y sin hacer de lado la importancia de satisfacer su mercado meta. A diferencia del artista que es más espontáneo, libre y sus acciones pueden no estar justificadas.</p>
