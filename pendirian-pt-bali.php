                    <div class="episode__content" style="border-top: none;">
                        <div class="title">&nbsp;</div>
                        <div class="story">
                            <h1 class="card__header-title">Mempersiapkan Data Pendirian PT </h1>
                            <p>
                                Sebelum membuat PT, Anda harus lebih dulu mempersiapkan
                                data-data berikut ini:
                            </p>
                            <ol class="numbered__list">
                                <li>
                                    <strong>Nama PT</strong>
                                    <p>
                                        Dalam memilih nama PT, Anda harus mengikuti panduan yang diatur dalam PP 43/2011 tentang Tata Cara Pengajuan dan Pemakaian Nama Perseroan Terbatas.
                                    <ul>
                                        <li>• Nama PT harus minimal terdiri dari 3 suku kata. </li>
                                        <li>• Nama PT harus menggunakan bahasa Indonesia.</li>
                                        <li>• Nama PT belum digunakan oleh PT lain.</li>
                                        <li>• Nama PT tidak menyerupai instansi pemerintah atau lembaga internasional.</li>
                                        <li>• Nama PT tidak boleh bertentangan dengan ketertiban umum dan kesusilaan.</li>
                                        <li>• Nama PT tidak boleh memiliki arti sebagai perseroan, badan hukum, atau persekutuan perdata. </li>
                                        <li>• Nama PT tidak boleh kata yang menggunakan maksud dan tujuan.</li>
                                    </ul>
                                    </p>
                                </li>
                                <li>
                                    <strong>Tempat dan Kedudukan PT</strong>
                                    <p>
                                        Tempat dan Kedudukan PT akan menjadi identitas lokasi di mana PT berada.
                                        Misalnya jika Anda memilih Kotamadya Jakarta Selatan sebagai tempat dan kedudukan PT, maka alamat PT harus ada di wilayah Jakarta Selatan.
                                        Dalam kasus ini apabila alamat PT Anda tidak berada di area tersebut, maka berdasarkan praktek akan dianggap sebagai cabang. Sehingga selanjutnya harus dibuat Akta Cabang dan diurus perizinannya.
                                    </p>
                                </li>
                                <li>
                                    <strong>Struktur Permodalan PT</strong>
                                    <p>
                                        Maksud dan tujuan PT akan menjelaskan tentang kegiatan apa saja yang dilakukan oleh PT, sebagaimana yang diatur dalam Pasal 3 Akta Pendirian PT.
                                        Beberapa hal ini penting dalam menentukan maksud dan tujuan PT:
                                        Pilih bidang usaha yang sesuai dengan PT Anda, perlu diperhatikan untuk tidak memilih yang dilarang oleh peraturan.
                                        Bidang usaha yang dijalankan wajib tertulis dalam akta pendirian PT.
                                        Izin usaha harus dimiliki oleh bidang usaha yang akan dijalankan. Misalnya jika Anda ingin menjual produk kecantikan Anda sendiri, maka Anda membutuhkan diantaranya izin BPOM.
                                    </p>
                                </li>
                                <li>
                                    <strong>Struktur Permodalan PT</strong>
                                    <p>
                                        Berdasarkan Undang-Undang No. 40 Tahun 2007 tentang Perseroan
                                        Terbatas, modal dalam mendirikan PT terbagi atas sekumpulan
                                        saham yang terdiri dari Modal Dasar, Modal Ditempatkan, dan
                                        Modal Disetor.
                                    <p>
                                        <strong>Modal Dasar :</strong>
                                        <span id="dots">...</span><span id="more">

                                            <br>
                                            • <strong>Modal Dasar</strong> adalah keseluruhan nilai PT. Dengan kata lain, besarnya sebuah PT dapat ditentukan berdasarkan permodalannya. Penilaian ini berfungsi untuk mengkategorikan kelas suatu PT. Modal Dasar merupakan seluruh nilai nominal saham. Berdasarkan UUPT, Modal Dasar minimal Rp 50.000.000, meskipun pada kegiatan usaha tertentu, besarnya Modal Dasar dapat ditentukan oleh undang-undang yang mengaturnya.
                                            <br>
